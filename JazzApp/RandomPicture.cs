        /// <summary>Returns a random integer number between 0 and an input number</summary>
        public int randomUniformInt(int i_max_number)
        {
            int ret_int = -12345;

            if (i_max_number < 0)
                return ret_int;

            double random_value = randomUniform();

            double ret_value = (double)i_max_number * random_value;

            ret_int = (int)ret_value;
            if (ret_int > i_max_number)
                ret_int = i_max_number;// Should never be the case but ....

            return ret_int;
        }

        /// <summary>Returns a random double between 0.0 and 1.0</summary>
        public double randomUniform()
        {
            double random_value = -0.123456789;

            int Z = 0;
            int k = 0;

            double d_temp1 = lSeed1 / 53668;
            k = (int)d_temp1;
            // k = Math.floor(lSeed1 / 53668);

            lSeed1 = 40014 * lSeed1 - k * 2147483563;

            if (lSeed1 < 0)
            {
                lSeed1 = lSeed1 + 2147483563;
            }

            double d_temp2 = lSeed2 / 52774;
            k = (int)d_temp2;
            // k = Math.floor(lSeed2 / 52774);

            lSeed2 = 40692 * lSeed2 - k * 2147483399;

            if (lSeed2 < 0)
            {
                lSeed2 = lSeed2 + 2147483399;
            }

            Z = (lSeed1 - lSeed2);

            if (Z < 1)
            {
                Z = Z + 2147483562;
            }

            random_value = Z * 4.656613e-10;

            return random_value;
        }

